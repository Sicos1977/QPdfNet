namespace QPdfNet.Enums;

/// <summary>
///     Rotation angle
/// </summary>
public enum Rotation
{
    /// <summary>
    ///     0 degrees clockwise
    /// </summary>
    Rotate0,

    /// <summary>
    ///     90 degrees clockwise
    /// </summary>
    Rotate90,

    /// <summary>
    ///     90 degrees counter clockwise
    /// </summary>
    RotateMinus90,

    /// <summary>
    ///     180 degrees clockwise
    /// </summary>
    Rotate180,

    /// <summary>
    ///     180 degrees counter clockwise
    /// </summary>
    RotateMinus180,

    /// <summary>
    ///     270 degrees clockwise
    /// </summary>
    Rotate270,

    /// <summary>
    ///     270 degrees counter clockwise
    /// </summary>
    RotateMinus270
}